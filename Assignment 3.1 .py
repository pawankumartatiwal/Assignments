# Dictionary 
student = {
    "name": "Pawan",
    "age": 20,
    "course": "Python"
}
print("Dictionary:", student)
print("Student name:", student["name"])

# Tuple 
colors = ("red", "green", "blue")
print("Tuple:", colors)
print("First color:", colors[0])

# Set 
fruits = {"apple", "banana", "mango", "apple"}  # 'apple' appears once only
print("Set:", fruits)
